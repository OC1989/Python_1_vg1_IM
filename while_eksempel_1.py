# Dette er et eksempel på en while-løkke i Python

# Sett en teller til 0
teller = 0

# Start en while-løkke som kjører så lenge telleren er mindre enn 5
while teller < 5:
    print("Telleren er nå:", teller)  # Skriv ut tellerverdien
    teller = teller + 1  # Øk telleren med 1

# Utskrift når løkken er ferdig
print("Løkken er ferdig.")
