# Eksempel på en for-løkke som itererer gjennom en liste

# Lag en liste med navn på frukt
frukt = ["eple", "banan", "appelsin", "jordbær", "drue"]

# Bruk en for-løkke til å skrive ut hvert element i listen
for element in frukt:
    print("Jeg liker " + element)

# Output vil være:
# Jeg liker eple
# Jeg liker banan
# Jeg liker appelsin
# Jeg liker jordbær
# Jeg liker drue
